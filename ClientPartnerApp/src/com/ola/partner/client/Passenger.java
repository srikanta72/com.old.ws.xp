package com.ola.partner.client;

public class Passenger {

	protected String passengerId;
	protected String passengerName;
	protected String passengerMobileNo;
	protected String passengerEmail;
	protected int noOfPassenger;
	public String getPassengerId() {
		return passengerId;
	}
	public void setPassengerId(String passengerId) {
		this.passengerId = passengerId;
	}
	public String getPassengerName() {
		return passengerName;
	}
	public void setPassengerName(String passengerName) {
		this.passengerName = passengerName;
	}
	public String getPassengerMobileNo() {
		return passengerMobileNo;
	}
	public void setPassengerMobileNo(String passengerMobileNo) {
		this.passengerMobileNo = passengerMobileNo;
	}
	public String getPassengerEmail() {
		return passengerEmail;
	}
	public void setPassengerEmail(String passengerEmail) {
		this.passengerEmail = passengerEmail;
	}
	public int getNoOfPassenger() {
		return noOfPassenger;
	}
	public void setNoOfPassenger(int noOfPassenger) {
		this.noOfPassenger = noOfPassenger;
	}
	
	
	
}
